package com.rizkykhapidsyah.forbersarang;

public class Main {
    public static void main(String[] args) {
        int a = 20;
        for (int i = 0; i <= a; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println("");
        }
    }
}
